const Mongoose = require("mongoose")
const crypto = require("crypto")

const roomSchema = new Mongoose.Schema( {
    title: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    maxPeople: {
      type: Number,
      required: true,
    },
    desc: {
      type: String,
      required: true,
    },
    roomNumbers: [{ number: Number, unavailableDates: {type: [Date]}}],
  },
  { timestamps: true }
  
  );

module.exports = Mongoose.model("room", roomSchema)
